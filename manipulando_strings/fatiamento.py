nome = "Julio Emílio Cerutti Lima"

print(nome[0]) # indice
print(nome[0:9]) # de 0 até o 9, não incluindo
print(nome[9:]) # a partir do índice 10, incluidon
print(nome[:]) # retorna a string
print(nome[6:12:2]) # retorna dentro do intervalo, de 2 em dois (step)
print(nome[::-1]) # retorna o inverso